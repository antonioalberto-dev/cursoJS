// ------ OPERADORES ARITMETICOS ----------------------------

let x = 2;
let y = 5;
let op;

op = x + y; // soma
console.log(op);
op = x - y; // subtracao
console.log(op);
op = x * y; // multiplicacao
console.log(op);
op = y / x; // divisao
console.log(op);
op = y**2; // potenciacao
console.log(op);
op = 5 % 2; //resto da divisao
console.log(op);

// -------- INCREMENTO e DECREMENTO ----------------------------------------
console.log('-------------------------------------------------');

console.log(x, ++x, x, ++x);
console.log(y, y--, y, --y);

console.log(op);
op += y;
console.log(op);

